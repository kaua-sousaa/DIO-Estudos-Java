package trueFalse;

public class Operacoes {
    public static void main(String[] args) {
        int numero = 5;
        
        System.out.println(numero++);
        System.out.println(numero);

        numero--;
        System.out.println(numero);

        //boolean
        boolean variavel = true;
        variavel = !variavel;
        System.out.println(!variavel);
    }
}
